public interface  Interface {
	//interface have only abstract method.
//	 While both are used to define types, they serve different purposes.
	//interface is not a class,it is a blue print for  class
	//method can use abstract keyword before the method name. but not mandatory.
	//method does not have body
	public abstract void iCar();
	public abstract void iDoor();
	public abstract void iWheel();
	public abstract String iName();
}
